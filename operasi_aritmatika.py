#operasi aritmatika dasar pada Python

#penjumlahan
bilangan_1 = 10
bilangan_2 = 20
hasil = bilangan_1 + bilangan_2
print("Hasil dari", bilangan_1, "+", bilangan_2, "adalah", hasil)

#pengurangan
bilangan_1 = 10
bilangan_2 = 20
hasil = bilangan_1 - bilangan_2
print("Hasil dari", bilangan_1, "-", bilangan_2, "adalah", hasil)

#perkalian
bilangan_1 = 10
bilangan_2 = 20
hasil = bilangan_1 * bilangan_2
print("Hasil dari", bilangan_1, "*", bilangan_2, "adalah", hasil)

#pembagian
bilangan_1 = 10
bilangan_2 = 20
hasil = bilangan_1 / bilangan_2
print("Hasil dari", bilangan_1, "/", bilangan_2, "adalah", hasil)

#pembagian pembulatan ke bawah
bilangan_1 = 10
bilangan_2 = 3
hasil = bilangan_1 // bilangan_2
print("Hasil dari", bilangan_1, "//", bilangan_2, "adalah", hasil)

#modulus
bilangan_1 = 10
bilangan_2 = 3
hasil = bilangan_1 % bilangan_2
print("Hasil dari", bilangan_1, "%", bilangan_2, "adalah", hasil)

#perpangkatan
bilangan_1 = 10
bilangan_2 = 3
hasil = bilangan_1 ** bilangan_2
print("Hasil dari", bilangan_1, "**", bilangan_2, "adalah", hasil)

#prioritas operasi, operational precedence
hasil = 3 + 2 * 4
print(hasil)


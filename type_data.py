#tipe data di python

#tipe data string
nama = "Budi"
umur = "10"
print(nama)

#tipe data integer
umur = 10
print(umur)

#tipe data float
tinggi = 175.5
print(tinggi)

#tipe data boolean
benar = True
salah = False
print(benar)
print(salah)

#tipe data list
nama = ["Budi", "Andi", "Caca"]
print(nama)

#tipe data tuple
nama = ("Budi", "Andi", "Caca")

#tipe data dictionary
siswa = {
    "nama": "Budi",
    "umur": 10,
    "alamat": "Jakarta"
}

#tipe data set
nama = {"Budi", "Andi", "Caca"}
print(nama)

#tipe data frozenset
nama = frozenset({"Budi", "Andi", "Caca"})
print(nama)

#tipe data bytes
data = b"Hello"
print(data)

#tipe data bytearray
data = bytearray(5)
print(data)

#tipe data memoryview
data = memoryview(b"Hello")
print(data)

#tipe data None
data = None
print(data)

#tipe data range
data = range(5)
print(data)

#tipe data complex
data = 5j
print(data)

